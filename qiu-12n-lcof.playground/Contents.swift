import UIKit

//面试题64. 求1+2+…+n
//求 1+2+...+n ，要求不能使用乘除法、for、while、if、else、switch、case等关键字及条件判断语句（A?B:C）。
//
//示例 1：
//输入: n = 3
//输出: 6
//
//示例 2：
//输入: n = 9
//输出: 45
//
//限制：
//1 <= n <= 10000


// ************************** 解法一  递归 **************************** //
/// 刚开始想到递归的，但是又想有没有更好的方案
class Solution {
    func sumNums(_ n: Int) -> Int {
        if n == 1 {
            return 1
        }
        return n+sumNums(n-1)
    }
}
